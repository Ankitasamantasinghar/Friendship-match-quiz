let currentQuestion = 0;
let score = 0;
let playerName = "";
let adminMode = false;

let selectedAnswers = [];

/* SOUND EFFECTS */

const correctSound = new Audio();
correctSound.src =
"https://assets.mixkit.co/active_storage/sfx/2000/2000-preview.mp3";

const clickSound = new Audio();
clickSound.src =
"https://assets.mixkit.co/active_storage/sfx/2568/2568-preview.mp3";

/* WRONG ANSWER SOUND */

const wrongSound = new Audio();
wrongSound.src =
"https://assets.mixkit.co/active_storage/sfx/1114/1114-preview.mp3";

/* NEXT BUTTON SOUND */

const nextSound = new Audio();
nextSound.src =
"https://assets.mixkit.co/active_storage/sfx/270/270-preview.mp3";


/* PRELOAD SOUNDS */

correctSound.load();
clickSound.load();
wrongSound.load();
nextSound.load();
/* ELEMENTS */

const startScreen =
document.getElementById("start-screen");

const quizScreen =
document.getElementById("quiz-screen");

const resultScreen =
document.getElementById("result-screen");

const questionEl =
document.getElementById("question");

const optionsEl =
document.getElementById("options");

const scoreEl =
document.getElementById("score");

const questionNumber =
document.getElementById("question-number");

/* START QUIZ */

function startQuiz(){

clickSound.play();

playerName =
document.getElementById("username").value;

if(playerName.trim() === ""){

alert("Please enter your name");
return;

}

startScreen.classList.add("hidden");

quizScreen.classList.remove("hidden");

loadQuestion();

}

/* LOAD QUESTION */

function loadQuestion(){

let q = questions[currentQuestion];

questionNumber.innerText =
`Question ${currentQuestion + 1} / ${questions.length}`;

questionEl.innerText = q.question;

optionsEl.innerHTML = "";

q.options.forEach(option => {

const card =
document.createElement("div");

card.classList.add("option-card");

if(selectedAnswers[currentQuestion]
=== option.text){

if(option.text === q.correct){

card.classList.add("correct");

}else{

card.classList.add("wrong");

}

}

card.innerHTML = `
<img src="${option.image}">
<h3>${option.text}</h3>
`;

card.onclick = () =>
checkAnswer(option.text);

optionsEl.appendChild(card);

});

scoreEl.innerText =
`Score: ${score}`;

}

function checkAnswer(answer){

if(selectedAnswers[currentQuestion]){
return;
}

clickSound.currentTime = 0;
clickSound.play();

selectedAnswers[currentQuestion] = answer;

const correctAnswer =
questions[currentQuestion].correct;

if(answer === correctAnswer){

correctSound.currentTime = 0;
correctSound.play();

}else{

wrongSound.currentTime = 0;
wrongSound.play();

}

calculateScore();

loadQuestion();

}

/* CALCULATE SCORE */

function calculateScore(){

score = 0;

questions.forEach((q,index)=>{

if(selectedAnswers[index]
=== q.correct){

score++;

}

});

}

function nextQuestion(){

nextSound.currentTime = 0;
nextSound.play();

if(currentQuestion <
questions.length - 1){

currentQuestion++;

loadQuestion();

window.scrollTo({
top:0,
behavior:"smooth"
});

}else{

showResult();

}

}

function previousQuestion(){

nextSound.currentTime = 0;
nextSound.play();

if(currentQuestion > 0){

currentQuestion--;

loadQuestion();

}

}
/* SHOW RESULT */

function showResult(){

quizScreen.classList.add("hidden");

resultScreen.classList.remove("hidden");

document.getElementById(
"final-score"
).innerText =

`${playerName},
Your Score is
${score} / ${questions.length}`;

let msg = "";

if(score >= 8){

msg = "🔥 Soul Bestie Match";

}
else if(score >= 5){

msg = "😄 Great Friendship Match";

}
else{

msg = "😂 Totally Opposite Choices";

}

document.getElementById(
"result-message"
).innerText = msg;

saveScore();

}

/* SAVE SCORE */

function saveScore(){

let leaderboard =
JSON.parse(
localStorage.getItem("leaderboard")
) || [];

leaderboard.push({
name: playerName,
score: score
});

localStorage.setItem(
"leaderboard",
JSON.stringify(leaderboard)
);

showLeaderboard();

}

/* SHOW LEADERBOARD */

function showLeaderboard(){

const leaderboardList =
document.getElementById(
"leaderboard-list"
);

leaderboardList.innerHTML = "";

let leaderboard =
JSON.parse(
localStorage.getItem("leaderboard")
) || [];

leaderboard.sort(
(a,b)=>b.score-a.score
);

leaderboard.forEach((player,index)=>{

const li =
document.createElement("li");

li.innerHTML = `
${player.name} —
${player.score}/${questions.length}

${adminMode
? `<button class="delete-btn"
onclick="deleteScore(${index})">
Delete
</button>`
: ""}
`;

leaderboardList.appendChild(li);

});

}

/* ADMIN LOGIN */

function adminLogin(){

clickSound.play();

const password =
prompt("Enter Admin Password");

if(password === "ankita123"){

adminMode = true;

alert("Admin Mode Activated");

showLeaderboard();

}else{

alert("Wrong Password");

}

}

/* DELETE SCORE */

function deleteScore(index){

clickSound.play();

let leaderboard =
JSON.parse(
localStorage.getItem("leaderboard")
) || [];

leaderboard.splice(index,1);

localStorage.setItem(
"leaderboard",
JSON.stringify(leaderboard)
);

showLeaderboard();

}

